#include "asm.h"

int trash(char *line, int i)
{
	int extra;

	extra = 0;
	while (line[i])
	{
		if (line[i] != ' ' && line[i] != '\t')
			return (extra);
		i++;
		extra++;
	}
	return (-1);
}
